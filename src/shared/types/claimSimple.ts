/**
 * Минимальный набор полей претензии.
 * Используется при связывании дефектов с претензиями.
 */
export interface ClaimSimple {
  /** Идентификатор претензии */
  id: number;
  /** Проект, к которому относится претензия */
  project_id: number | null;
  /** Объекты, связанные с претензией */
  unit_ids: number[];
  /** Список идентификаторов дефектов */
  defect_ids: number[];
}
